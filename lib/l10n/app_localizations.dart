  String text(String key, [Map<String, Object?> params = const {}]) {
    final languageCode = _localizedValues.containsKey(locale.languageCode)
        ? locale.languageCode
        : 'en';

    var value =
        _localizedValues[languageCode]?[key] ??
        _localizedValues['en']?[key] ??
        key;

    params.forEach((paramKey, paramValue) {
      value = value.replaceAll('{$paramKey}', paramValue.toString());
    });

    return value;
  }

  String term(String value) {
    final key = 'term.$value';
    final translated = text(key);
    return translated == key ? value : translated;
